package io.github.astrapi69.mystic.crypt.enumtype;

/**
 * The enum {@link FrameMode} provides information in which mode the current frame is
 */
public enum FrameMode {

    /**
     * This mode indicates that current frame has as main component a {@link javax.swing.JDesktopPane}
     */
    DESKTOP_PANE,

    /**
     * This mode indicates that current frame has as main component a {@link io.github.astrapi69.swing.base.BasePanel}
     */
    APPLICATION_PANEL
}
